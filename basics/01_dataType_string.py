# 02. 문자열 자료형

## 문자열을 만드는 방법
# 1. 큰따옴표(" ")
print("Hello World")
# 2. 작은따옴표(' ')
print('Python is fun')
# 3. 큰따옴표 연속 3개
print("""Life is too short, You need python""")
# 4. 작은따옴표 연속 3개
print('''Life is too short, You need python''')

## 문자열 안에 큰/작은따옴표를 포함시키고 싶을 때
# 1. 작은따옴표 포함
print("Python's favorite food is perl")
# 2. 큰따옴표 포함
print('"Python is very easy." he says.')
# 3. 역슬래시 사용
print('Python\'s favorite food is perl')
print("\"Python is very easy.\" he says.")